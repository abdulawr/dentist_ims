package com.ss_technology.dims.Adapter;

public class Medician_Container {
    private String name;
    private String mg;
    private String type;
    private String duration;
    private String description;
    private String time_in_day;
    private String taking_time;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getMg() {
        return mg;
    }

    public void setMg(String mg) {
        this.mg = mg;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getDuration() {
        return duration;
    }

    public void setDuration(String duration) {
        this.duration = duration;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public String getTime_in_day() {
        return time_in_day;
    }

    public void setTime_in_day(String time_in_day) {
        this.time_in_day = time_in_day;
    }

    public String getTaking_time() {
        return taking_time;
    }

    public void setTaking_time(String taking_time) {
        this.taking_time = taking_time;
    }
}
